import java.util.*;

public class Q06 {
    public static void main(String[] args) {
        int x = 0, y = 0;
        // turn
        // travel
        x += 10;

        // upward turn
        // travel 20 upward
        y += 20;

        // turn left
        // travel 30 leftward
        x -= 30;

        // downward turn
        // travel 40 down
        y -= 40;

        // turn left
        // travel 50
        x += 50;

        System.out.println(x + " , " + y);

    }
}
